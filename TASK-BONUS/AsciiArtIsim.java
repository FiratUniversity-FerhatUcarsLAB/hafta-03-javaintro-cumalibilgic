public class AsciiArtIsim {

    public static void main(String[] args) {

        // --- Bonus Görev 4: ASCII Sanatı ---

        System.out.println("--- Bonus 4: ASCII Sanatı ---");
        System.out.println("Merhaba Cumali, işte ismin:");
        System.out.println(); // Estetik için bir boşluk bırakalım

        // Her System.out.println() bir satırı temsil eder.
        // Harfleri yan yana getirmek için her satırı birleştiriyoruz.

        // C     U    U   M   M    A      L       IIIII
        System.out.println("  #####  #    #  #   #   ####   #        #####");
        // C     U    U   M M M   A A     L         I
        System.out.println(" #       #    #  ## ##  #    #  #          #  ");
        // C     U    U   M M M  AAAAA    L         I
        System.out.println(" #       #    #  # # #  #    #  #          #  ");
        // C     U    U   M   M  A   A    L         I
        System.out.println(" #       #    #  #   #  #    #  #          #  ");
        //  #####   UUU   M   M  A   A    LLLLL   IIIII
        System.out.println("  #####   ####   #   #  #    #  ######   #####");

        System.out.println(); // Sona da bir boşluk bırakalım
    }
}